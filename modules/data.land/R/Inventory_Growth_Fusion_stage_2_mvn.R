##' @name InventoryGrowthFusion
##' @title InventoryGrowthFusion
##' @description this code fuses forest inventory data with tree growth data (tree ring or dendrometer band)
##' for the same plots. Code is a rewrite of Clark et al 2007 Ecol Appl into JAGS
##' 
##' @param dbh.only.data  list of data inputs from trees with only dbh measurements (no increment cores)
##' @param posterior.estimates  posterior estimates of all model parameters from the first stage to be used as informative priors for 2nd stage
##' @param random = whether or not to include random effects
##' @param n.chunk number of MCMC steps to evaluate at a time. Will only return LAST. If restarting, second number in vector is chunk to start from
##' @param n.burn  number of steps to automatically discard as burn-in
##' @param save.state whether or not to include inferred DBH in output (can be large). Enter numeric value to save.state periodically (in terms of n.chunk)
##' @param restart  final mcmc.list from previous execution. NULL for new run. TRUE to save final state for new run.
##' @note Requires JAGS
##' @return an mcmc.list object
##' @export
InventoryGrowthFusion_stage2 <- function(data, posterior.estimates = NULL, cov.data=NULL, time_data = NULL, informative.time = TRUE, informative.site = TRUE, informative.plot =TRUE, n.iter=5000, n.chunk = n.iter, n.burn = min(n.chunk, 2000), random = NULL, fixed = NULL,time_varying=NULL, burnin_plot = FALSE, output.folder= "/home/rstudio/pecan/IGF_PIPO_AZ_mcmc/", save.jags = "IGF.ragged.txt", model.name = "model",z0 = NULL, save.state=TRUE, restart = NULL, breakearly = TRUE) {
  
  
  
  library(rjags)
  print(paste("start of MCMC", Sys.time()))
  
  # baseline variables to monitor  
  burnin.variables <- c("tau_add", "tau_dbh", "tau_inc", "mu") # process variability, dbh and tree-ring observation error, intercept
  out.variables <- c("deviance", "tau_add", "tau_dbh", "tau_inc", "mu")
  # if(save.state) out.variables <- c(out.variables,"x")
  if(!exists("model")) model = 0
  
  ## restart
  if(length(n.chunk)>1){
    k_restart = n.chunk[2]
    n.chunk = n.chunk[1]
  } else {
    k_restart = 1
  }
  max.chunks <- ceiling(n.iter/n.chunk)
  if(max.chunks < k_restart){
    PEcAn.utils::logger.warn("MCMC already complete",max.chunks,k_restart)
    return(NULL)
  }
  avail.chunks <- k_restart:ceiling(n.iter/n.chunk)
  
  check.dup.data <- function(data,loc){
    if(any(duplicated(names(data)))){print("duplicated variable at",loc,names(data))}
  }  
  # start text object that will be manipulated (to build different linear models, swap in/out covariates)  
  TreeDataFusionMV <- "
model{

  ### Loop over all individuals
  for(i in 1:ni){
  
  #### Data Model: DBH
  for(t in 1:nt){
  z[i,t] ~ dnorm(x[i,t],tau_dbh)
  }
  
  #### Data Model: growth
  for(t in 2:nt){
  inc[i,t] <- x[i,t]-x[i,t-1]
  y[i,t] ~ dnorm(inc[i,t],tau_inc)
  }
  
  #### Process Model
  for(t in 2:nt){
  Dnew[i,t] <- x[i,t-1] + mu ##PROCESS
  x[i,t]~dnorm(Dnew[i,t],tau_add)
  }
  
  ## initial condition
  x[i,1] ~ dnorm(x_ic,tau_ic)
  }  ## end loop over individuals

  ## RANDOM_EFFECTS
  
  #### Priors
  tau_dbh ~ dnorm(a_dbh,r_dbh)
  tau_inc ~ dnorm(a_inc,r_inc)
  tau_add ~ dnorm(a_add,r_add)
  mu ~ dnorm(0.5,0.5)
  ## FIXED EFFECTS BETAS
  ## ENDOGENOUS BETAS
  ## TIME VARYING BETAS
  ## RANDOM EFFECT TAUS
 }"
  ########################################################################
  #
  # get summary of posterior estimates from previous model
  #
  ########################################################################
  # these will be used to specify informative priors below:
  
  Pformula <- NULL
  ########################################################################
  ###
  ###        RANDOM EFFECTS
  ###
  ########################################################################
  if (!is.null(random)) {
    Rpriors <- NULL
    Reffects <- NULL
    ## parse random effects
    r_vars <- gsub(" ","",unlist(strsplit(random,"+",fixed=TRUE))) ## split on +, remove whitespace
    for(i in seq_along(r_vars)){
      ## special case: individidual
      if(r_vars[i] == "i"){
        r_var   <- "i"
        counter <- ""
        index   <- "i"
        nr <- nrow(cov.data)
      } else if(r_vars[i] == "t"){
        r_var   <- "t"
        counter <- ""
        index   <- "t"
        nr <- ncol(cov.data)
      } else {
        index <- counter <- nr <- NA
        r_var <- gsub("(","",gsub(")","",r_vars[i],fixed = TRUE),fixed="TRUE")
        r_var <- strsplit(r_var,"|",fixed=TRUE)[[1]]
        fix   <- r_var[1]
        ## check for nested effects
        r_var <- strsplit(gsub("\\",":",r_var[2],fixed=TRUE),":",fixed = TRUE)[[1]]
        for(j in seq_along(length(r_var))){
          if(j>1)print("WARNING: not actually nesting random effects at this time")            ## HACK: to get started, not actually nesting
          ## parse
          j_var      <- strsplit(r_var[j],"[",fixed = TRUE)[[1]]
          index[j]   <- gsub("]","",j_var[2],fixed=TRUE)
          counter[j] <- j_var[1]
          r_var[j]   <- j_var[1]
          ## add variable to data
          if(!(r_var[j] %in% names(data))){
            data[[length(data)+1]] <- as.numeric(as.factor(as.character(cov.data[,r_var[j]]))) ## multiple conversions to eliminate gaps
            names(data)[length(data)] <- r_var[j]
          }
          check.dup.data(data,"r_var")
          nr[j] <- max(as.numeric(data[[r_var[j]]]))
        }
        index <- paste0("[",index,"]")
      }
      ## create formula
      Pformula <- paste(Pformula,
                        paste0("+ alpha_", r_var,"[",counter,index,"]"))
      ## create random effect
      for(j in seq_along(nr)){
        
        Reffects <- paste(Reffects,
                          paste0("for(k in 1:",nr[j],"){\n"),
                          paste0("   alpha_",r_var[j],"[k] ~ dnorm(0,tau_",r_var[j],")\n}\n"))
      }
      ## create priors
      if(informative.plot == FALSE){
        Rpriors <- paste(Rpriors,paste0("tau_",r_var," ~ dgamma(1,0.1)\n",collapse = " "))
      }else{
        Rpriors <- paste(Rpriors,paste0("tau_",r_var," ~ dnorm(",posterior.summary[posterior.summary$parameter %in% "tau_PLOT", ]$means ,",", (1/posterior.summary[posterior.summary$parameter %in% "tau_PLOT", ]$vars) ,")\n",collapse = " "))
      }
      ## track
      burnin.variables <- c(burnin.variables, paste0("tau_", r_var))
      out.variables <- c(out.variables, paste0("tau_", r_var), paste0("alpha_",r_var))
      
    }
    ## Substitute into code
    TreeDataFusionMV <- sub(pattern = "## RANDOM EFFECT TAUS", Rpriors, TreeDataFusionMV)
    TreeDataFusionMV <- gsub(pattern = "## RANDOM_EFFECTS", Reffects, TreeDataFusionMV)
  }   ### END RANDOM EFFECTS
  
  ########################################################################
  ###
  ###        FIXED EFFECTS
  ###
  ########################################################################
  if(FALSE){
    ## DEV TESTING FOR X, polynomial X, and X interactions
    fixed <- "X + X^3 + X*bob + bob + dia + X*Tmin[t]" ## faux model, just for testing jags code
  }
  ## Design matrix
  if (is.null(fixed)) {
    Xf <- NULL
  } else {
    
    ## check for covariate data (note: will falsely fail if only effect is X)
    if (is.null(cov.data)) {
      print("formula provided but covariate data is absent:", fixed)
    } else {
      cov.data <- as.data.frame(cov.data)
    }
    
    ## check if there's a tilda in the formula
    if (length(grep("~", fixed)) == 0) {
      fixed <- paste("~", fixed)
    }
    
    ### BEGIN adding in tree size (endogenous variable X)    
    ## First deal with endogenous terms (X and X*cov interactions)
    fixedX <- sub("~","",fixed, fixed=TRUE)
    lm.terms <- gsub("[[:space:]]", "", strsplit(fixedX,split = "+",fixed=TRUE)[[1]])  ## split on + and remove whitespace
    X.terms <- strsplit(lm.terms,split = c("^"),fixed = TRUE)
    X.terms <- sapply(X.terms,function(str){unlist(strsplit(str,,split="*",fixed=TRUE))})
    X.terms <- which(sapply(X.terms,function(x){any(toupper(x) == "X")}))    
    if(length(X.terms) > 0){
      ## rebuild fixed without X.terms
      fixed <- paste("~",paste(lm.terms[-X.terms],collapse = " + "))  
      
      ## isolate terms with X
      X.terms <- lm.terms[X.terms]
      Xpriors <- NULL
      for(i in seq_along(X.terms)){
        
        myBeta <- NULL
        Xformula <- NULL
        if(length(grep("*",X.terms[i],fixed = TRUE)) == 1){  ## INTERACTION
          
          myIndex <- "[i]"
          covX <- strsplit(X.terms[i],"*",fixed=TRUE)[[1]] 
          covX <- covX[-which(toupper(covX)=="X")] ## remove X from terms
          
          ##is covariate fixed or time varying?
          tvar <-  length(grep("[t]",covX,fixed=TRUE)) > 0
          if(tvar){
            covX <- sub("[t]","",covX,fixed = TRUE)
            if(!(covX %in% names(data))){
              ## add cov variables to data object
              data[[covX]] <- time_data[[covX]]
            }
            check.dup.data(data,"covX")
            
            myIndex <- "[i,t]"
          } else {
            ## variable is fixed
            if(covX %in% colnames(cov.data)){ ## covariate present
              if(!(covX %in% names(data))){
                ## add cov variables to data object
                data[[covX]] <- cov.data[,covX]
              }
              check.dup.data(data,"covX2")
              
            } else {
              ## covariate absent
              print("covariate absent from covariate data:", covX)
            }
            
          } ## end fixed or time varying
          
          myBeta <- paste0("betaX_",covX)
          Xformula <- paste0(myBeta,"*x[i,t-1]*",covX,myIndex)
          
        } else if(length(grep("^",X.terms[i],fixed=TRUE))==1){  ## POLYNOMIAL
          powX <- strsplit(X.terms[i],"^",fixed=TRUE)[[1]] 
          powX <- powX[-which(toupper(powX)=="X")] ## remove X from terms
          myBeta <- paste0("betaX",powX)
          Xformula <- paste0(myBeta,"*x[i,t-1]^",powX)
          
        } else {  ## JUST X
          myBeta <- "betaX"
          Xformula <- paste0(myBeta,"*x[i,t-1]")
        }
        
        ## add variables to Pformula
        Pformula <- paste(Pformula,"+",Xformula)
        
        ## add priors, make them infomative if informative.site ==TRUE
        
        if(informative.site == TRUE){
          Xpriors <- paste0(Xpriors,"     ",myBeta, "~dnorm(", posterior.summary[posterior.summary$parameter %in% myBeta,]$means ,",",1/(posterior.summary[posterior.summary$parameter %in% myBeta,]$var ),")", collapse="\n")
        }else{
          Xpriors <- paste(Xpriors,"     ",myBeta,"~dnorm(0,0.001)\n")
        }
        ## add to out.variables
        out.variables <- c(out.variables, myBeta)
        
      }  ## END LOOP OVER X TERMS
      
      ## create priors
      TreeDataFusionMV <- sub(pattern = "## ENDOGENOUS BETAS", Xpriors, TreeDataFusionMV)
      
    }  ## end processing of X terms
    
    ## build design matrix from formula
    Xf      <- with(cov.data, model.matrix(formula(fixed)))
    Xf.cols <- colnames(Xf)
    Xf.cols <- sub(":","_",Xf.cols) ## for interaction terms, switch separator
    colnames(Xf) <- Xf.cols
    Xf.cols <- Xf.cols[Xf.cols != "(Intercept)"]
    Xf      <- as.matrix(Xf[, Xf.cols])
    colnames(Xf) <- Xf.cols
    ##Center the covariate data
    Xf.center <- apply(Xf, 2, mean, na.rm = TRUE)
    Xf      <- t(t(Xf) - Xf.center)
  }  ## end fixed effects parsing
  
  ## build formula in JAGS syntax
  if (!is.null(Xf)) {
    Xf.names <- gsub(" ", "_", colnames(Xf))  ## JAGS doesn't like spaces in variable names
    ## append to process model formula
    Pformula <- paste(Pformula,
                      paste0("+ beta", Xf.names, "*Xf[rep[i],", seq_along(Xf.names), "]", collapse = " "))
    ## create 'rep' variable if not defined
    if(is.null(data$rep)){
      data$rep <- seq_len(nrow(Xf))
    }
    ## create priors
    
    if(informative.site == TRUE){
      # make informative priors, if informative flag is true
      Xf.priors <-  paste0("     beta", Xf.names[1], "~dnorm(", posterior.summary[posterior.summary$parameter %in% paste0("beta", Xf.names[1]),]$means ,",",1/(posterior.summary[posterior.summary$parameter %in% paste0("beta", Xf.names[1]),]$var ),")", "\n",
                           "     beta", Xf.names[2], "~dnorm(", posterior.summary[posterior.summary$parameter %in% paste0("beta", Xf.names[2]),]$means ,",",1/(posterior.summary[posterior.summary$parameter %in% paste0("beta", Xf.names[2]),]$var ),")", collapse="\n")
      
      
    }else{
      Xf.priors <- paste0("     beta", Xf.names, "~dnorm(0,0.001)", collapse = "\n")
    }
    
    
    TreeDataFusionMV <- sub(pattern = "## FIXED EFFECTS BETAS", Xf.priors, TreeDataFusionMV)
    ## update variables for JAGS to track
    data[["Xf"]] <- Xf
    out.variables <- c(out.variables, paste0("beta", Xf.names))
  }
  
  check.dup.data(data,"Xf")
  
  ########################################################################
  ###
  ###        TIME-VARYING
  ###
  ########################################################################
  
  if(FALSE){ # always false...just for development
    ## DEVEL TESTING FOR TIME VARYING
    #time_varying <- "TminJuly + PrecipDec + TminJuly*PrecipDec"
    time_varying <- "tmax_Jun + ppt_Dec + tmax_Jun*ppt_Dec" 
    time_data <- list(TminJuly = matrix(0,4,4),PrecipDec = matrix(1,4,4))
  }
  
  if(!is.null(time_varying)){
    if (is.null(time_data)) {
      print("time_varying formula provided but time_data is absent:", time_varying)
    }
    Xt.priors <- ""    
    
    ## parse equation into variable names
    t_vars <- gsub(" ","",unlist(strsplit(time_varying,"+",fixed=TRUE))) ## split on +, remove whitespace
    ## check for interaction terms
    it_vars <- t_vars[grep(pattern = "*",x=t_vars,fixed = TRUE)]
    if(length(it_vars) > 0){
      t_vars <- t_vars[!(t_vars %in% it_vars)]
    } 
    
    ## INTERACTIONS WITH TIME-VARYING VARS
    ## TODO: deal with interactions with catagorical variables
    ## need to create new data matrices on the fly
    for(i in seq_along(it_vars)){
      
      ##is covariate fixed or time varying?
      covX <- strsplit(it_vars[i],"*",fixed=TRUE)[[1]] 
      tvar    <- length(grep("[t]",covX[1],fixed=TRUE)) > 0
      tvar[2] <- length(grep("[t]",covX[2],fixed=TRUE)) > 0
      myBeta <- "beta"
      for(j in 1:2){
        if(j == 2) myBeta <- paste0(myBeta,"_")
        if(tvar[j]){
          covX[j] <- sub("[t]","",covX[j],fixed = TRUE)
          if(!(covX[j] %in% names(data))){
            ## add cov variables to data object
            data[[covX[j]]] <- time_data[[covX[j]]]
          }
          myBeta <- paste0(myBeta,covX[j])
          covX[j] <- paste0(covX[j],"[i,t]")
        } else {
          ## variable is fixed
          if(!(covX[j] %in% names(data))){
            ## add cov variables to data object
            data[[covX[j]]] <- cov.data[,covX[j]]
          }
          myBeta <- paste0(myBeta,covX[j])
          covX[j] <- paste0(covX[j],"[i]")
        } ## end fixed or time varying
        
      } ## end building beta
      
      ## append to process model formula
      Pformula <- paste(Pformula,
                        paste0(" + ",myBeta,"*",covX[1],"*",covX[2]))
      
      ## priors
      if(informative.time == TRUE){
        # make informative priors, if informative flag is true
        Xt.priors <- paste0(Xt.priors,"     ", myBeta, "~dnorm(", posterior.summary[posterior.summary$parameter %in% myBeta,]$means,",",(1/posterior.summary[posterior.summary$parameter %in% myBeta,]$var) ,")", collapse="\n")
      }else{
        Xt.priors <- paste0(Xt.priors,
                            "    ",myBeta,"~dnorm(0,0.001)\n")
      }
      
      
      #Xt.priors <- paste0(Xt.priors,
      #  "    ",myBeta,"~dnorm(0,0.001)\n")
      
      # Xt.priors <- paste0(Xt.priors,"     ", myBeta, "~dnorm(", posterior.summary[posterior.summary$parameter %in% myBeta,]$means ,",",1/(posterior.summary[posterior.summary$parameter %in% myBeta,]$var ),")", collapse="\n")
      
      ## add to list of varibles JAGS is tracking
      out.variables <- c(out.variables, myBeta)
      
    }  ## end time-varying interaction terms
    
    
    ## loop over variables
    for(j in seq_along(t_vars)){
      tvar <- t_vars[j]
      
      if(!(tvar %in% names(data))){
        ## add cov variables to data object
        data[[tvar]] <- time_data[[tvar]]
      }
      check.dup.data(data,"tvar")
      
      ## append to process model formula
      Pformula <- paste(Pformula,
                        paste0("+ beta", tvar, "*",tvar,"[i,t]"))
      
      ## add to list of varibles JAGS is tracking
      out.variables <- c(out.variables, paste0("beta", tvar))
    }
    ## build prior
    
    
    if(informative.time == TRUE){
      # make informative priors, if informative flag is true
      # Xt.priors <- paste0(Xt.priors,
      #                     paste0("     beta", t_vars, "~dunif(", paste(quantile(rnorm(1000,posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars),]$means,(posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars),]$var)), 0)) ,",",paste(quantile(rnorm(1000,posterior.summary[posterior.summary$parameter %in% myBeta,]$mean,(posterior.summary[posterior.summary$parameter %in% myBeta,]$var)), 1)) ,")", collapse="\n"))
      #  
      Xt.priors <- paste0(Xt.priors,
                          paste0("     beta", t_vars[1], "~dnorm(", posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars[1]),]$means, ",",(1/posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars[1]),]$var),")", "\n",
                                 "     beta", t_vars[2], "~dnorm(",posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars[2]),]$means, ",",(1/posterior.summary[posterior.summary$parameter %in% paste0("beta", t_vars[2]),]$var),")",collapse="\n"))
      
      
    }else{
      Xt.priors <- paste0(Xt.priors,
                          "     beta", t_vars,"~dnorm(0,0.001)\n")
    }
    
    TreeDataFusionMV <- sub(pattern = "## TIME VARYING BETAS", Xt.priors, TreeDataFusionMV)
    
  } ## END time varying covariates
  
  
  ## insert process model into JAGS template
  if (!is.null(Pformula)) {
    TreeDataFusionMV <- sub(pattern = "##PROCESS", Pformula, TreeDataFusionMV)
  }
  
  ## save script 
  if(!is.null(save.jags)){
    cat(TreeDataFusionMV,file=save.jags)
  }
  
  ## state variable initial condition
  if(is.null(z0)){
    z0 <- t(apply(data$y, 1, function(y) {
      -rev(cumsum(rev(y)))
    })) + data$z[, ncol(data$z)]
  } 
  
  ## JAGS initial conditions
  init   <- list()
  source("pecan/modules/data.land/R/mcmc.list2initIGF.R") # use the new specific mcmc.list2initIGF.R
  if(is.mcmc.list(restart)){
    init <- mcmc.list2initIGF(restart)
    nchain <- length(init)
  } else {
    nchain <- 3
    for (i in seq_len(nchain)) {
      y.samp <- sample(data$y, length(data$y), replace = TRUE)
      z0ragged <- z0 # lines for z0ragged come from mikes "fix" to help with tree w/no cores
      for(j in 1:data$ni){ # 1: number of individuals
        # this creates z0ragged, where we only have z0 for trees w/ cores during the time period of the cores &
        # we only have z0 for trees w/out cores after the time where we have DBH measurements.
        # this could help with model fitting
        if(data$startyr[j]>1){
          z0ragged[j,1:(data$startyr[j]-1)] <- NA
        }
        if(data$endyr[j]<data$nt){
          z0ragged[j,(data$endyr[j]+1):data$nt] <- NA
        }
      }
      init[[i]] <- list(x = z0ragged, 
                        tau_add = runif(1, 1, 5) / var(diff(y.samp), na.rm = TRUE),
                        tau_dbh = posterior.summary[posterior.summary$parameter %in% "tau_dbh",]$means, 
                        tau_inc = posterior.summary[posterior.summary$parameter %in% "tau_inc",]$means,
                        #tau_ind = posterior.summary[posterior.summary$parameter %in% "tau_inc",]$means, 
                        #tau_yr = 100,
                        #betaX2 = 0, 
                        ind = rep(0, data$ni),  
                        year = rep(0, data$nt))
    }
  }
  
  #-----------------------------------------------------------------------
  # make sure we also use the values for informative normal priors on taus:
  #-----------------------------------------------------------------------
  data$a_add <- posterior.summary[posterior.summary$parameter %in% "tau_add",]$means
  data$r_add <- 1/posterior.summary[posterior.summary$parameter %in% "tau_add",]$vars
  
  data$a_inc <- posterior.summary[posterior.summary$parameter %in% "tau_inc",]$means
  data$r_inc <- 1/posterior.summary[posterior.summary$parameter %in% "tau_inc",]$vars
  
  data$a_dbh <- posterior.summary[posterior.summary$parameter %in% "tau_dbh",]$means
  data$r_dbh <- 1/posterior.summary[posterior.summary$parameter %in% "tau_dbh",]$vars
  
  
  
  means <- apply(as.matrix(posterior.ests), 2, mean)
  vars <- apply(as.matrix(posterior.ests), 2, var)
  SD <- apply(as.matrix(posterior.ests), 2, sd)
  
  # generate data frame with a summary of the posterior estimates
  posterior.summary <- data.frame(means = apply(as.matrix(posterior.ests), 2, mean),
                                  vars = apply(as.matrix(posterior.ests), 2, var),
                                  SD = apply(as.matrix(posterior.ests), 2, sd))
  posterior.summary$parameter <- rownames(posterior.summary)
  
  
  reduced <- posterior.ests[[1]]
  
  posterior.cov.matrix <- cov(posterior.ests[[1]]) # should use all the chains....
  
  posterior.mean.matrix <- colMeans(posterior.ests[[1]])
  
  # get only the params of interest:
  posterior.mean.matrix <- posterior.mean.matrix [1:11]
  posterior.cov.matrix <- posterior.cov.matrix [1:11, 1:11]
  
  posterior.mean.matrix <- posterior.mean.matrix[!names(posterior.mean.matrix) %in% c("mu", "deviance")]
  posterior.cov.matrix <-posterior.cov.matrix [!rownames(posterior.cov.matrix) %in% c("mu", "deviance"),!colnames(posterior.cov.matrix) %in% c("mu", "deviance")]
  
  
  posterior.prec.matrix <- 1/posterior.cov.matrix 
  
  data$posterior.param.means = c(posterior.mean.matrix)
  data$sigma.posterior = posterior.cov.matrix
  data$P <- length(colnames(data$sigma.posterior))
  # test model:
  
  print("COMPILE JAGS MODEL")    
  #j.model <- jags.model(file = textConnection(TreeDataFusionMV), data = data, inits = init, n.chains = 3)
  j.model <- jags.model(file = "stage2_tunc_infom_nvm.txt", data = data, inits = init, n.chains = 3)
  
  if(n.burn > 0){
    print("BURN IN")
    jags.out <- coda.samples(model = j.model, 
                             variable.names = burnin.variables, 
                             n.iter = n.burn)
    if (burnin_plot) {
      plot(jags.out)
    }
  }
  
  print("RUN MCMC")
  load.module("dic")
  for(k in avail.chunks){
    
    ## determine whether to sample states
    if(as.logical(save.state) & k%%as.numeric(save.state) == 0){
      vnames <- c("x",out.variables)   ## save x periodically (this actually always saves x, from my experience)
    } else {
      vnames <- out.variables
    }
    
    ## sample chunk
    jags.out <- coda.samples(model = j.model, variable.names = vnames, n.iter = n.chunk)
    
    ## save chunk
    ofile <- paste0(output.folder, model.name ,".",k,".RData")
    print(ofile)
    save(jags.out,file=ofile)
    
    ## update restart
    if(!is.null(restart) & ((is.logical(restart) && restart) || is.mcmc.list(restart))){
      ofile <- paste0(output.folder,"IGF",".",model.name,".","RESTART.RData")
      jags.final <- coda.samples(model = j.model, variable.names = c("x",out.variables), n.iter = 1)
      k_restart = k + 1  ## finished k, so would restart at k+1
      save(jags.final,k_restart,file=ofile)
    }
    if(breakearly == TRUE){
      ## check for convergence and break from loop early
      D <- as.mcmc.list(lapply(jags.out,function(x){x[,'deviance']}))
      gbr <- coda::gelman.diag(D)$psrf[1,1]
      trend <- mean(sapply(D,function(x){coef(lm(x~seq_len(n.chunk)))[2]}))
      if(gbr < 1.005 & abs(trend) < 0.5) break
    }
  }
  
  print(paste("end of MCMC", Sys.time()))
  return(jags.out)
} # InventoryGrowthFusion
